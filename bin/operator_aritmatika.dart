void main() {
  var first = 11;
  var second = 8;

  print(first + second);        //pertambahan
  print(first - second);        //- maka pengurangan jika -1 maka negatif
  print(first * second);        //perkalian
  print(first / second);        //pembagian hasilnya double
  print(first ~/ second);       //pembagian hasilnya int
  print(first % second);        //sisa bagi
}
